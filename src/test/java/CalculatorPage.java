public class CalculatorPage {
    public int getSum(int number1,int number2) {
        return number1+number2;
    }
    public int getResultOfMultiplication(int number1, int number2) {

        return number1*number2;

    }
    public int getResultOfDivision(int number1,int number2) {
        return number1/number2;
    }
    public int getDifference(int number1, int number2) {

        return number1 - number2;
    }
}
